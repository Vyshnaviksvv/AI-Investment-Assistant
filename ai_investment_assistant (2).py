def fetch_stock_data(ticker, period="1y"):
    """
    Retrieves historical price data from Yahoo Finance.
    Defaults to 1 year of daily closing prices.
    """
    try:
        stock = yf.Ticker(ticker)
        # Fetch 2 years to ensure we have a full 200-day MA padding even with a 1-year analysis period
        df = stock.history(period="2y" if period=="1y" else period)
        if df.empty:
            print(f"⚠️ Warning: No historical data returned for ticker '{ticker}'.")
            return None
        return df
    except Exception as e:
        print(f"❌ Error querying yfinance for ticker {ticker}: {e}")
        return None

def calculate_indicators(df):
    """
    Applies custom technical analysis indicators on the price DataFrame.
    Calculates SMAs, RSI, MACD, and Bollinger Bands using Pandas/NumPy.
    """
    df = df.copy()
    if len(df) < 200:
        raise ValueError("Dataframe must contain at least 200 trading days to construct moving averages.")

    # 1. Moving Averages (50-day and 200-day Simple Moving Average)
    df['MA50'] = df['Close'].rolling(window=50).mean()
    df['MA200'] = df['Close'].rolling(window=200).mean()

    # 2. Bollinger Bands (20-day SMA + 2 Standard Deviations)
    df['MA20'] = df['Close'].rolling(window=20).mean()
    df['BB_std'] = df['Close'].rolling(window=20).std()
    df['BB_Upper'] = df['MA20'] + (df['BB_std'] * 2)
    df['BB_Lower'] = df['MA20'] - (df['BB_std'] * 2)

    # 3. Relative Strength Index (RSI, 14-day Wilder's smoothed)
    delta = df['Close'].diff()
    gain = delta.clip(lower=0)
    loss = -delta.clip(upper=0)

    # Wilder's exponential moving average smoothing
    avg_gain = gain.ewm(com=13, adjust=False).mean()
    avg_loss = loss.ewm(com=13, adjust=False).mean()

    rs = np.where(avg_loss == 0, 100, avg_gain / avg_loss)
    df['RSI'] = 100 - (100 / (1 + rs))

    # 4. Moving Average Convergence Divergence (MACD)
    df['EMA12'] = df['Close'].ewm(span=12, adjust=False).mean()
    df['EMA26'] = df['Close'].ewm(span=26, adjust=False).mean()
    df['MACD'] = df['EMA12'] - df['EMA26']
    df['MACD_Signal'] = df['MACD'].ewm(span=9, adjust=False).mean()
    df['MACD_Hist'] = df['MACD'] - df['MACD_Signal']

    return df

def generate_technical_recommendation(df):
    """
    Evaluates latest indicators and computes a rules-based rating
    and confidence score.
    """
    latest = df.iloc[-1]
    prev = df.iloc[-2]

    signals = {}
    weights = {
        'rsi': 0.25,
        'macd': 0.25,
        'ma_align': 0.30,
        'bb': 0.20
    }

    # 1. RSI Score
    rsi = latest['RSI']
    if rsi < 30:
        signals['rsi'] = (1.0, "Oversold (RSI < 30) - Bullish Indicator")
    elif rsi > 70:
        signals['rsi'] = (-1.0, "Overbought (RSI > 70) - Bearish Indicator")
    elif rsi < 40:
        signals['rsi'] = (0.5, "Moderate Bullish Accumulation Zone")
    elif rsi > 60:
        signals['rsi'] = (-0.5, "Moderate Bearish Distribution Zone")
    else:
        signals['rsi'] = (0.0, "Neutral Momentum")

    # 2. MACD Score
    macd = latest['MACD']
    sig = latest['MACD_Signal']
    hist = latest['MACD_Hist']
    prev_hist = prev['MACD_Hist']

    if hist > 0 and prev_hist <= 0:
        signals['macd'] = (1.0, "Bullish MACD Crossover")
    elif hist < 0 and prev_hist >= 0:
        signals['macd'] = (-1.0, "Bearish MACD Crossover")
    elif macd > sig:
        signals['macd'] = (0.5, "MACD above Signal (Bullish Outlook)")
    else:
        signals['macd'] = (-0.5, "MACD below Signal (Bearish Outlook)")

    # 3. SMA Trend Alignment Score
    price = latest['Close']
    ma50 = latest['MA50']
    ma200 = latest['MA200']

    if price > ma50 > ma200:
        signals['ma_align'] = (1.0, "Golden Trend Alignment (Price > MA50 > MA200)")
    elif price < ma50 < ma200:
        signals['ma_align'] = (-1.0, "Bearish Trend Alignment (Price < MA50 < MA200)")
    elif price > ma50:
        signals['ma_align'] = (0.5, "Short term bullish (Price above MA50)")
    else:
        signals['ma_align'] = (-0.5, "Short term bearish (Price below MA50)")

    # 4. Bollinger Bands Score
    bb_upper = latest['BB_Upper']
    bb_lower = latest['BB_Lower']
    bb_middle = latest['MA20']
    bb_range = bb_upper - bb_lower
    bb_pos = (price - bb_lower) / bb_range if bb_range != 0 else 0.5

    if price <= bb_lower or bb_pos < 0.1:
        signals['bb'] = (1.0, "Price near/below Lower Bollinger Band (Extreme Oversold)")
    elif price >= bb_upper or bb_pos > 0.9:
        signals['bb'] = (-1.0, "Price near/above Upper Bollinger Band (Extreme Overbought)")
    else:
        signals['bb'] = (0.0, f"Neutral BB range (Position: {bb_pos:.1%})")

    # Calculate weighted score
    raw_score = sum(signals[key][0] * weights[key] for key in weights) * 100

    # Recommendation boundaries
    if raw_score >= 25:
        recommendation = "BUY"
    elif raw_score <= -25:
        recommendation = "SELL"
    else:
        recommendation = "HOLD"

    # Scale confidence score
    confidence = int(abs(raw_score))
    confidence = max(20, min(95, confidence)) # bound between 20% and 95%

    return {
        "recommendation": recommendation,
        "confidence_score": confidence,
        "raw_score": raw_score,
        "signals": {k: {"score": signals[k][0], "reason": signals[k][1]} for k in signals},
        "metrics": {
            "current_price": price,
            "ma50": ma50,
            "ma200": ma200,
            "rsi": rsi,
            "macd": macd,
            "macd_signal": sig,
            "bb_upper": bb_upper,
            "bb_lower": bb_lower,
            "bb_middle": bb_middle
        }
    }

