def plot_technical_analysis(ticker, df):
    """
    Renders a unified 3-panel professional dashboard plot.
    """
    # View the last 120 trading days (roughly 6 months of data) for clean visuals
    plot_df = df.iloc[-120:]

    fig, axes = plt.subplots(3, 1, figsize=(14, 12), sharex=True,
                             gridspec_kw={'height_ratios': [2.5, 0.9, 0.9]})

    fig.suptitle(f"{ticker} - Multi-Indicator Technical Analysis Dashboard",
                 fontsize=15, fontweight='bold', color='#1A202C', y=0.98)

    # Axis 0: Price Action, MAs & BBs
    ax0 = axes[0]
    ax0.plot(plot_df.index, plot_df['Close'], label='Closing Price', color='#2B6CB0', linewidth=2)
    ax0.plot(plot_df.index, plot_df['MA50'], label='50-day SMA', color='#D69E2E', linestyle='--', linewidth=1.5)
    ax0.plot(plot_df.index, plot_df['MA200'], label='200-day SMA', color='#E53E3E', linestyle='-.', linewidth=1.5)
    ax0.plot(plot_df.index, plot_df['BB_Upper'], label='BB Upper (2.0 SD)', color='#4A5568', linestyle=':', linewidth=1)
    ax0.plot(plot_df.index, plot_df['BB_Lower'], label='BB Lower (2.0 SD)', color='#4A5568', linestyle=':', linewidth=1)
    ax0.fill_between(plot_df.index, plot_df['BB_Lower'], plot_df['BB_Upper'], color='#CBD5E0', alpha=0.25, label='BB Channel')

    ax0.set_ylabel('Price', fontsize=11, fontweight='semibold')
    ax0.legend(loc='upper left', frameon=True, facecolor='white', edgecolor='#E2E8F0', fontsize=9)
    ax0.set_title("Price Action & Trend Structures", fontsize=11, fontweight='bold', loc='left', pad=6)
    ax0.tick_params(axis='y', labelsize=9)

    # Axis 1: RSI
    ax1 = axes[1]
    ax1.plot(plot_df.index, plot_df['RSI'], label='RSI (14)', color='#805AD5', linewidth=1.5)
    ax1.axhline(70, color='#E53E3E', linestyle='--', linewidth=1.2)  # Overbought line
    ax1.axhline(30, color='#38A169', linestyle='--', linewidth=1.2)  # Oversold line
    ax1.fill_between(plot_df.index, 30, 70, color='#805AD5', alpha=0.06)
    ax1.set_ylabel('RSI', fontsize=11, fontweight='semibold')
    ax1.set_ylim(15, 85)
    ax1.legend(loc='upper left', frameon=True, facecolor='white', edgecolor='#E2E8F0', fontsize=9)
    ax1.set_title("Relative Strength Index (RSI)", fontsize=11, fontweight='bold', loc='left', pad=6)
    ax1.tick_params(axis='y', labelsize=9)

    # Axis 2: MACD
    ax2 = axes[2]
    ax2.plot(plot_df.index, plot_df['MACD'], label='MACD Line', color='#3182CE', linewidth=1.5)
    ax2.plot(plot_df.index, plot_df['MACD_Signal'], label='Signal Line (9)', color='#DD6B20', linewidth=1.5)

    # Color MACD Hist columns
    hist_colors = ['#38A169' if val >= 0 else '#E53E3E' for val in plot_df['MACD_Hist']]
    ax2.bar(plot_df.index, plot_df['MACD_Hist'], label='Histogram', color=hist_colors, alpha=0.6, width=0.8)

    ax2.set_ylabel('MACD', fontsize=11, fontweight='semibold')
    ax2.set_xlabel('Timeline', fontsize=11, fontweight='semibold')
    ax2.legend(loc='upper left', frameon=True, facecolor='white', edgecolor='#E2E8F0', fontsize=9)
    ax2.set_title("MACD (12, 26, 9) Momentum Oscillator", fontsize=11, fontweight='bold', loc='left', pad=6)
    ax2.tick_params(axis='y', labelsize=9)

    plt.tight_layout()
    plt.show()

